.MODEL SMALL ;SCOPE OF CODE

.STACK 100H ;ALLOCATE MEMORY IN HEXADECIMAL

.DATA ;VARIABLE DECLARATION
CR EQU 0DH
LF EQU 0AH


.CODE

MAIN PROC ; LIKE MAIN

;DATA SEGMENT INIT

MOV AX, @DATA;BRINGS DATA TO AX FOR INIT
MOV DS, AX     


MOV AH,2
MOV DL,0
MOV BX,256

PRINT: 
INT 21H
INC DL
DEC BX
JNZ PRINT
    
;DOES EXIT   
    MOV AH,4CH
    INT 21H ;INTERRUPT FUNCTION


MAIN ENDP


END MAIN;END OF CODE