.MODEL SMALL ;SCOPE OF CODE

.STACK 100H ;ALLOCATE MEMORY IN HEXADECIMAL

.DATA ;VARIABLE DECLARATION
CR EQU 0DH
LF EQU 0AH




.CODE

MAIN PROC ; LIKE MAIN

;DATA SEGMENT INIT

MOV AX, @DATA;BRINGS DATA TO AX FOR INIT
MOV DS, AX  

MOV AX,0FFFFH
MOV BX,01H
ADD AX,BX

MOV AX,01
MOV BX,02
SUB AX,BX

MOV AX,07FFFH
MOV BX,07FFFH
ADD AX,BX



;DOES EXIT
MOV AH,4CH
INT 21H ;INTERRUPT FUNCTION


MAIN ENDP


END MAIN;END OF CODE

