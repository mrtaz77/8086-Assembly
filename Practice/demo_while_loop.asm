.MODEL SMALL ;SCOPE OF CODE

.STACK 100H ;ALLOCATE MEMORY IN HEXADECIMAL

.DATA ;VARIABLE DECLARATION
CR EQU 0DH
LF EQU 0AH
NL DB CR,LF,'$'

.CODE

MAIN PROC ; LIKE MAIN

;DATA SEGMENT INIT

MOV AX, @DATA;BRINGS DATA TO AX FOR INIT
MOV DS, AX     

MOV BX,0
MOV AH,1


WHILE:

INT 21H 
INC BX
CMP AL,CR
JNE WHILE


EXIT:
LEA DX,NL
MOV AH,9
INT 21H

MOV DX,BX
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H ;


MAIN ENDP


END MAIN;END OF CODE