.MODEL SMALL ;SCOPE OF CODE

.STACK 100H ;ALLOCATE MEMORY IN HEXADECIMAL

.DATA ;VARIABLE DECLARATION
CR EQU 0DH
LF EQU 0AH
NL DB CR,LF,'$'
ERR DB  CR,LF,'No capital letters$'
SM DB CR,LF,'Smallest : $'
LG DB CR,LF,'Largest : $'

.CODE

MAIN PROC ; LIKE MAIN

;DATA SEGMENT INIT

MOV AX, @DATA;BRINGS DATA TO AX FOR INIT
MOV DS, AX     

XOR BX,BX
MOV BH,0FFH

MOV AH,1

WHILE:
INT 21H

CMP AL,'A'
JB CHECK
CMP AL,'Z'
JA CHECK


CMP BH,AL
JBE SMALL
MOV BH,AL


SMALL:
CMP BL,AL
JAE CHECK
MOV BL,AL

CHECK:
CMP AL,CR
JNE WHILE

CMP BL,0
JE EQ

LEA DX,LG
MOV AH,9
INT 21H

MOV DL,BL
MOV AH,2
INT 21H

LEA DX,SM
MOV AH,9
INT 21H

MOV DL,BH
MOV AH,2
INT 21H 
JMP EXIT

EQ:
LEA DX,ERR
MOV AH,9
INT 21H

EXIT:

MOV AH,4CH
INT 21H 


MAIN ENDP


END MAIN;END OF CODE